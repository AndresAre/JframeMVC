    private void jCbCarreraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCbCarreraActionPerformed
        String carrera = jCbCarrera.getSelectedItem().toString();
        int vlrCredito;
        switch(carrera){
            case "Sistemas":vlrCredito=345000;
                            break;
            case "Contabilidad":vlrCredito=287900;
                            break;
            case "Administracion":vlrCredito=178900;
                            break;
            case "Medicina":vlrCredito=450000;
                            break;
            default: vlrCredito=0;
        }
        jTxVlrCredito.setText(String.valueOf(vlrCredito));
    }//GEN-LAST:event_jCbCarreraActionPerformed

